<h4>WP Cloudy Custom Template for Developers</h4>
<p>You can create a completely personalized weather without changing the core of the plugin. In addition, your changes will not be overwritten at each plugin update.</p>
<ul>
  <li>Go to WP Cloudy template directory: http://yoursite.com/wp-content/plugins/wp-cloudy-2/template/</li>
  <li>Copy content-wpcloudy-custom1.php and content-wpcloudy-custom2.php files.</li>
  <li>Go to your theme directory: http://yoursite.com/wp-content/themes/yourtheme/</li>
  <li>Create a new folder called wp-cloudy-2 (the spelling is important).</li>
  <li>In this new folder, paste content-wpcloudy-custom1.php and content-wpcloudy-custom2.php files.</li>
  <li>Now, open content-wpcloudy-custom1.php or content-wpcloudy-custom2.php with a php editor like notepad ++, coda2, sublime textâ€¦</li>
  <li>Write your weather layout.. You can use the other templates as guidelines.</li>
  <li>Save and send your changes via FTP.</li>
  <li>Now you built a custom weather template. Select the template "Custom 1" or "Custom 2" in your weather settings.</li>
</ul>
